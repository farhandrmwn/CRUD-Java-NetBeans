    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(TxtNis.getText().equals("")||TxtNama.getText().equals("")){
            JOptionPane.showMessageDialog(null,"isi textbox");
        }else{
            try{
                int sr=tb.getSelectedRow();
                DefaultTableModel model = (DefaultTableModel) tb.getModel();
                sql="UPDATE tbl_siswa SET nama='"+TxtNama.getText()+"'WHERE nis='"+model.getValueAt(sr,0)+"'";
                PreparedStatement ps=con.prepareStatement(sql);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Berhasil Update Data");
                kosong();
                tampil();
                TxtNis.setText("");
                TxtNama.setText("");

            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, "GAGAL Menemukan Data\n"+e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
        int row=tb.getSelectedRow();
        String value=(tb.getModel().getValueAt(row,0).toString());
        String sql="DELETE FROM tbl_siswa WHERE nis="+value; 
        PreparedStatement ps=con.prepareStatement(sql);
        ps.executeUpdate();
        JOptionPane.showMessageDialog(null,"Berhasil Hapus Data");
        kosong();
        tampil();
    }catch(SQLException e){
         JOptionPane.showMessageDialog(this, "GAGAL Menemukan Data\n"+e.getMessage());
    }
    }//GEN-LAST:event_jButton3ActionPerformed

