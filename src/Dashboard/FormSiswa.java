    private void kosong(){
    DefaultTableModel model=(DefaultTableModel) tb.getModel();
    model.setRowCount(0);
    }
    
    private void tampil(){
    DefaultTableModel model=(DefaultTableModel) tb.getModel();
    try{
        sql="SELECT * FROM tbl_siswa";
        java.sql.Statement stm = con.createStatement();
        java.sql.ResultSet rs = stm.executeQuery(sql);
        while(rs.next()){
            model.addRow(new Object[]{rs.getString(1),rs.getString(2)});
        }
        tb.setModel(model);
    }catch(Exception e){
        
    }
    }
    
    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        // TODO add your handling code here:
        if(TxtNis.getText().equals("")||TxtNama.getText().equals("")){
       JOptionPane.showMessageDialog(null,"isi data yang lengkap");
       }else{
        try{
            sql="INSERT INTO tbl_siswa VALUES('"+TxtNis.getText()+"','"+TxtNama.getText()+"')";
            stat.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
            kosong();
            tampil();
            TxtNis.setText("");
            TxtNama.setText("");
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "GAGAL Menyimpan Data\n"+e.getMessage());
}
       }
    }//GEN-LAST:event_BtnAddActionPerformed

